# Operadores Python

# Operadores Aritméticos
# Operadores aritméticos são usados com valores numéricos para realizar operações matemáticas comuns:

#   Operator	Name	Example	Try it
"""
      +	    Addition	     x + y	
      -	    Subtraction	     x - y	
      *	    Multiplication	 x * y	
      /	    Division	     x / y	
      %	    Modulus	         x % y	
     **	    Exponentiation	 x ** y	
     //	    Floor division	 x // y
"""
# Exemplos de operadores aritméticos:
soma = 10 + 5
print(f"A soma de 10 e 5 é: {soma}")  # returns 15

subtracao = 10 - 5
print(f"A subtração de 10 e 5 é: {subtracao}")  # returns 5

multiplicacao = 10 * 5
print(f"A multiplicação de 10 e 5 é: {multiplicacao}")  # returns 50

divisao = 10 / 5
print(f"A divisão de 10 e 5 é: {divisao}")  # returns 2.0

modulo = 10 % 3
print(f"O módulo de 10 e 3 é: {modulo}")  # returns 1

exponenciacao = 10 ** 2
print(f"A exponenciação de 10 elevado a 2 é: {exponenciacao}") # returns 100

divisao_inteira = 10 // 3
print(f"A divisão inteira de 10 por 3 é: {divisao_inteira}") # returns 3
