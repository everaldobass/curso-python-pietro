# Operadores de Atribuição
# Operadores de atribuição são usados para atribuir valores a variáveis:

#   Operator	Example	Same As
"""    =	x = 5	x = 5	
      +=	x += 3	x = x + 3	
      -=	x -= 3	x = x - 3	
      *=	x *= 3	x = x * 3	
      /=	x /= 3	x = x / 3	
      %=	x %= 3	x = x % 3	
     **=	x **= 3	x = x ** 3	
     //=	x //= 3	x = x // 3
"""

# Exemplos de operadores de atribuição:
x = 5 # Atribuição simples
print(f"O valor de x é: {x}")  # returns 5

x += 3 # Atribuição de adição
print(f"O valor de x após x += 3 é: {x}")  # returns

x -= 2 # Atribuição de subtração
print(f"O valor de x após x -= 2 é: {x}")  # returns

x *= 2 # Atribuição de multiplicação
print(f"O valor de x após x *= 2 é: {x}")  # returns

x /= 4 # Atribuição de divisão
print(f"O valor de x após x /= 4 é: {x}")  # returns

x %= 3 # Atribuição de módulo
print(f"O valor de x após x %= 3 é: {x}")  # returns

x **= 2 # Atribuição de exponenciação
print(f"O valor de x após x **= 2 é: {x}")  # returns

x //= 2 # Atribuição de divisão inteira
print(f"O valor de x após x //= 2 é: {x}")  # returns



